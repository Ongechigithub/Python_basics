# arithmetic operators

x = 7
y = 3
print(x + y)  # add
print(x - y)  # minus
print(x * y)  # multiply
print(x / y)  # division
print(x % y)  # reminder

# comparison operator

a = 12
b = 34

print(f"{a} is equal to {b} {a == b}")
print(f"{a} is not equals to {b} {a != b}")
print(f"{a} is greater than {b} {a > b}")
print(f"{a} is greater than or equals to {b} {a >= b}")
print(f"{a} is less than {b} {a < b}")
print(f"{a} is less than or equals to {b} {a <= b}")

# logical operator
c = 5
print(f"is this statement true {c>2 and c<5}")
print(f"one of the condition is true {c>2 or c<=5}")
